        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(
            content: Text(
              'Some error occured',
              style: TextStyle(
                color: FlutterFlowTheme.of(context).primaryText,
              ),
            ),
            duration: const Duration(milliseconds: 4000),
            backgroundColor: FlutterFlowTheme.of(context).secondary,
          ),
        );
